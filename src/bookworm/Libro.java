
package bookworm;

public class Libro {
    String nombre;
    String genero;

    public Libro(String nombre, String genero) {
        this.nombre = nombre;
        this.genero = genero;
    }

    public String getNombre() {
        return nombre;
    }
    public String getGenero() {
        return genero;
    }
    
    
    
    
}
